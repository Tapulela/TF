    @Override
    public void actualizarUnObjeto(Object unObjeto) {
        
        if (unObjeto instanceof OrdenDeCompra){
            this.unaOrdenDeCompra = (OrdenDeCompra) unObjeto;
            exhibirOrdenDeCompra();
            if (jCBOperacion.getSelectedItem().equals("Registrar"))
                habilitarSeleccionCriterio();
        }
        if (unObjeto instanceof Equipamiento){
            this.unEquipamiento = (Equipamiento) unObjeto;
            exhibirEquipamiento();
        }
        if (unObjeto instanceof Lote){
            this.unLote = (Lote) unObjeto;
            exhibirLote();
            if (jCBOperacion.getSelectedItem().equals("Registrar"))
                habilitarSeleccionCriterio();
        }
        if (unObjeto instanceof AnalisisLaboratorio){
            this.unObjetoSeleccionado = (AnalisisLaboratorio) unObjeto;
            exhibirAnalisis();
            if (!(jCBOperacion.getSelectedItem().equals("Anular"))){
                habilitarRadioButtons();
            }
            habilitarConcresion();
            
        }
        if (unObjeto instanceof CriterioAnalisisLaboratorio){
            this.unCriterioSeleccionado = (CriterioAnalisisLaboratorio) unObjeto;
            exhibirCriterio();
            habilitarConcresion();
        }
    }

