    private void jBConcretarAccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBConcretarAccionActionPerformed
       
        try {
            switch ((String)jCBOperacion.getSelectedItem()){
                case "Registrar":
                    if (jRadioButton1.isSelected())
                        unaOrdenDeCompra = null;
                    if (jRadioButton2.isSelected())
                        unLote = null;
                    this.organizacion.registrarAnalisisDeLaboratorio((String) jCB1.getSelectedItem(), (String) jCB2.getSelectedItem(), (String) jCB3.getSelectedItem(), (String) jCB4.getSelectedItem(), (String) jCB5.getSelectedItem(), (String) jCB6.getSelectedItem(), jC1.getCalendar(), unLote, this.unEquipamiento, jTFCampo2.getText(), jTFCampo1.getText(), jTFCampo3.getText(), jTFCampo5.getText(), jTFCampo4.getText(), this.unCriterioSeleccionado, jTADescripcion.getText(), unaOrdenDeCompra);
                    break;
                case "Modificar":
                    if (jRadioButton1.isSelected())
                        unaOrdenDeCompra = null;
                    if (jRadioButton2.isSelected())
                        unLote = null;
                    this.organizacion.modificarAnalisisLaboratorio(unObjetoSeleccionado, unLote, unaOrdenDeCompra);
                    break;
                case "Anular":
                    this.organizacion.anularAnalisisDeLaboratorio(unObjetoSeleccionado);
                    break;
            }
            JOptionPane.showMessageDialog(null, "Operación realizada con exito.");
            deshabilitarTodo();
            limpiarCampos();
            habilitarCamposIniciales();
        } catch (ExcepcionCargaParametros ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error en la base de datos: "+ex.getMessage());
        } catch (ExcepcionPersistencia ex) {
            JOptionPane.showMessageDialog(null, "Error en la persistencia: "+ex.getMessage());
        }
        
    }//GEN-LAST:event_jBConcretarAccionActionPerformed

