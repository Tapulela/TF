            jLStaticEtiqueta6.setText(unObjetoSeleccionado.getLoteImplicado().getEtiqueta());
        if (this.unObjetoSeleccionado.getOrdenDeCompraImplicada() != null)
            jLStaticEtiqueta16.setText(""+unObjetoSeleccionado.getOrdenDeCompraImplicada().getId());
