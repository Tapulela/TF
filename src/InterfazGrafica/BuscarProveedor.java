    public BuscarProveedor(Organizacion organizacion, JFrame ventanaAnterior, String trayectoriaAnterior) {
        
        this.setUndecorated(true);
        initComponents();
        this.setSize(Toolkit.getDefaultToolkit().getScreenSize());
        this.setVisible(true); 
        this.ventanaAnterior = ventanaAnterior;
        this.ventanaAnterior.setFocusable(false);
        this.organizacion = organizacion;
        this.trayectoriaActual = trayectoriaAnterior + " - Busqueda de Proveedor";
        
        
        
        cabeceraDeVentana.configurarCabecera(ventanaAnterior, this, "Busqueda de un Proveedor", this.trayectoriaActual, organizacion.getUsuarioActivo().getApellido()+", "+organizacion.getUsuarioActivo().getNombre());
        jTable1.setRowHeight(30);
        setIconImage(new ImageIcon(getClass().getResource(ParametrosDeInterfaz.rutaIcono)).getImage());
        this.getContentPane().setBackground(ParametrosDeInterfaz.colorFondo);
        
        
        
        
        criteriosSeleccionados = new HashMap<String, Boolean>();
        criteriosSeleccionados.put("razonSocial", false);
        criteriosSeleccionados.put("cuit", false);
        criteriosSeleccionados.put("estado", false);
        if (ventanaAnterior instanceof ABMProveedor){
            ABMProveedor abmProveedor = (ABMProveedor) ventanaAnterior;
            if (abmProveedor.getOperacionActual().equals("Baja")){
                //Si voy a dar de baja, solo puedo seleccionar proveedor activo.
                jCBEstado.doClick();
                jCBEstado.setEnabled(false);
                jComboBox2.setSelectedItem("Activo");
                jComboBox2.setEnabled(false);
            }
        }
        
        if (ventanaAnterior instanceof GestionOrdenesCompra){
                //Si elijo un proveedor para una orden de compra, solo puedo elegir activos.
                jCBEstado.doClick();
                jCBEstado.setEnabled(false);
                jComboBox2.setSelectedItem("Activo");
                jComboBox2.setEnabled(false);            
            
        }
        if (ventanaAnterior instanceof GestionIngresoMP){
                //Si elijo un proveedor de servicio de transporte para un ingreso, solo puedo elegir activos.
                jCBEstado.doClick();
                jCBEstado.setEnabled(false);
                jComboBox2.setSelectedItem("Activo");
                jComboBox2.setEnabled(false);            
            
        }        
        establecerAlineacionDeTabla(jTable1, SwingConstants.CENTER);
        ParametrosDeInterfaz.configurarVentana(this);
    }
