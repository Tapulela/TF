    private void limpiarCampos() {
                this.operacionActual = "";
                
                jTFCampo1.setText("");
                jTFCampo2.setText("");
                jTFCampo3.setText("");
                
                jCB1.setSelectedItem("Seleccionar");
                this.unObjetoSeleccionado = null;
    }

    @Override
    public void actualizarUnObjeto(Object unObjeto) {
        this.unObjetoSeleccionado = (Usuario) unObjeto;
        jTFCampo4.setText(this.unObjetoSeleccionado.getNombreUsuario());
        jTFCampo1.setText(this.unObjetoSeleccionado.getNombre());
        jTFCampo2.setText(this.unObjetoSeleccionado.getApellido());
        jTFCampo3.setText(this.unObjetoSeleccionado.getApellido());
        jCBEstado.setSelectedItem(this.unObjetoSeleccionado.getEstado());
        jCB1.setSelectedItem(this.unObjetoSeleccionado.getRol());
        
        organizarElementos();
        
        
        jBConcretarAccion.setEnabled(true);
    }

