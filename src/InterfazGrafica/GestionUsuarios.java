    private void jBConcretarAccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBConcretarAccionActionPerformed
       
        try {
            switch ((String)jCBOperacion.getSelectedItem()){
                case "Alta":
                    if (Arrays.equals(jPasswordField1.getPassword(), jPasswordField2.getPassword()))
                        organizacion.darDeAltaUnUsuario(jTFCampo4.getText(), jTFCampo1.getText(), jTFCampo2.getText(), jTFCampo3.getText(), (String) jCB1.getSelectedItem());
                    else
                        throw new ExcepcionCargaParametros("Las contraseñas ingresadas no coinciden.");
                    break;
                case "Baja":
                    organizacion.darDeBajaUnUsuario(this.unObjetoSeleccionado);
                    break;
                case "Modificacion":
                    organizacion.modificarUnUsuario(unObjetoSeleccionado, (String)jCBEstado.getSelectedItem(), (String)jCB1.getSelectedItem());
                    break;
            }
            JOptionPane.showMessageDialog(null, "Operación realizada con exito.");
            deshabilitarTodo();
            limpiarCampos();
            habilitarCamposIniciales();
        } catch (ExcepcionCargaParametros ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "error en la base de datos: "+ex.getMessage());
        } catch (ExcepcionPersistencia ex) {
            JOptionPane.showMessageDialog(null, "error en la persistencia: "+ex.getMessage());
        }
        
    }//GEN-LAST:event_jBConcretarAccionActionPerformed

