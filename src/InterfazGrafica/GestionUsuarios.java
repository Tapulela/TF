        jBBuscar.setVisible(true);
        jBBuscar.setEnabled(true);
        jBCancelar.setEnabled(true);
        
        jLStaticCampo4.setVisible(false);
        jLStaticCampo6.setVisible(false);
        jPasswordField1.setVisible(false);
