    private void exhibirCriterioDeAnalisisDeLaboratorio() {
        if (this.unObjetoSeleccionado == null)
            return;
        jTFCampo1.setText(unObjetoSeleccionado.getNombre());
        jC1.setCalendar(unObjetoSeleccionado.getFechaOrigenC());
        jTADescripcion.setText(unObjetoSeleccionado.getDescripcion());
        jTFCampo2.setText(""+unObjetoSeleccionado.getPorcentajeSemillaLimiteInferior());
        jTFCampo3.setText(""+unObjetoSeleccionado.getPorcentajeSemillaLimiteSuperior());
        jTFCampo4.setText(""+unObjetoSeleccionado.getPorcentajePaloLimiteInferior());
        jTFCampo5.setText(""+unObjetoSeleccionado.getPorcentajePaloLimiteSuperior());
        jTFCampo6.setText(""+unObjetoSeleccionado.getPorcentajePolvoLimiteInferior());
        jTFCampo7.setText(""+unObjetoSeleccionado.getPorcentajePolvoLimiteSuperior());
        jTFCampo8.setText(""+unObjetoSeleccionado.getPorcentajeHojaLimiteInferior());
        jTFCampo9.setText(""+unObjetoSeleccionado.getPorcentajeHojaLimiteSuperior());
        jTFCampo10.setText(""+unObjetoSeleccionado.getPorcentajeHumedadLimiteInferior());
        jTFCampo11.setText(""+unObjetoSeleccionado.getPorcentajeHumedadLimiteSuperior());
        
        jCB1.setSelectedItem(unObjetoSeleccionado.getPuntosNegros());
        jCB2.setSelectedItem(unObjetoSeleccionado.getTorrada());
        jCB3.setSelectedItem(unObjetoSeleccionado.getColor());
        jCB4.setSelectedItem(unObjetoSeleccionado.getAroma());
        jCB5.setSelectedItem(unObjetoSeleccionado.getTacto());
        jCB6.setSelectedItem(unObjetoSeleccionado.getDegustacion());
        jCB6.setSelectedItem(unObjetoSeleccionado.getTipoLote());
    }

