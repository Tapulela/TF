    private void jBConcretarAccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBConcretarAccionActionPerformed
       
        try {
            switch ((String)jCBOperacion.getSelectedItem()){
                case "Alta":
                    this.organizacion.registrarCriterioDeAnalisisDeLaboratorio(jTFCampo1.getText(), jTADescripcion.getText(), jC1.getCalendar(),
                            (String) jCB7.getSelectedItem(), (String) jCB1.getSelectedItem(), (String) jCB2.getSelectedItem(), (String) jCB3.getSelectedItem(), (String) jCB4.getSelectedItem(), (String) jCB5.getSelectedItem(), (String) jCB6.getSelectedItem(), jTFCampo2.getText(), jTFCampo3.getText(), jTFCampo4.getText(), jTFCampo5.getText(), jTFCampo6.getText(), jTFCampo7.getText(), jTFCampo10.getText(), jTFCampo11.getText(), jTFCampo8.getText(), jTFCampo9.getText());
                    break;
                case "Baja":
                    this.organizacion.darDeBajaUnCriterio(this.unObjetoSeleccionado);
                    break;
                case "Modificacion":
                    this.organizacion.modificarCriterioDeAnalisis(this.unObjetoSeleccionado, (String) jCBEstado.getSelectedItem());
                    break;
            }
            JOptionPane.showMessageDialog(null, "Operación realizada con exito.");
            deshabilitarTodo();
            limpiarCampos();
            habilitarCamposIniciales();
        } catch (ExcepcionCargaParametros ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error en la base de datos: "+ex.getMessage());
        } catch (ExcepcionPersistencia ex) {
            JOptionPane.showMessageDialog(null, "Error en la persistencia: "+ex.getMessage());
        }
        
    }//GEN-LAST:event_jBConcretarAccionActionPerformed

