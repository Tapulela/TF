    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            if (unProveedorSeleccionado == null)
                throw new ExcepcionCargaParametros("Seleccione un proveedor para generar su estadística.");
            Map<String, String> criterios = new HashMap<String, String>();
            Map<String, Boolean> criterioss = new HashMap<String, Boolean>();
            ArrayList listaTuplas = Estadistica.generarTuplasProveedor(unProveedorSeleccionado);
            GeneradorDeReportes.generarReporteGenericoEstadistica("Proveedor "+unProveedorSeleccionado.getRazonSocial(), criterioss, criterios, listaTuplas, organizacion.getUsuarioActivo().getNombre()+" "+organizacion.getUsuarioActivo().getApellido());
        } catch (ExcepcionCargaParametros ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (JRException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

