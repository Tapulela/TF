    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            if (unProveedorSeleccionado == null)
                throw new ExcepcionCargaParametros("Seleccione un proveedor para generar su estadística.");
            jPanel6.removeAll();
            ChartPanel unPanelGrafico = Estadistica.generarPanelGraficoTorta(unProveedorSeleccionado.calcularAnalisisAprobadosYRechazados(), "Cantidad de análisis aprobados y rechazados", 1.5f);
            Estadistica.agregarChartPanel(jPanel6, unPanelGrafico);
            jLEstaticoTamanoMuestra1.setText(unProveedorSeleccionado.obtenerCalidad());
        } catch (ExcepcionCargaParametros ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

